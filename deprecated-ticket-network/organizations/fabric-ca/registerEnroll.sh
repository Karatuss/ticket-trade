#!/bin/bash

function createSeller() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/seller.ticket.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/seller.ticket.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-seller --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-seller.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-seller.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-seller.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-seller.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/seller.ticket.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy seller's CA cert to seller's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/seller.ticket.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/seller/ca-cert.pem" "${PWD}/organizations/peerOrganizations/seller.ticket.com/msp/tlscacerts/ca.crt"

  # Copy seller's CA cert to seller's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/seller.ticket.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/seller/ca-cert.pem" "${PWD}/organizations/peerOrganizations/seller.ticket.com/tlsca/tlsca.seller.ticket.com-cert.pem"

  # Copy seller's CA cert to seller's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/seller.ticket.com/ca"
  cp "${PWD}/organizations/fabric-ca/seller/ca-cert.pem" "${PWD}/organizations/peerOrganizations/seller.ticket.com/ca/ca.seller.ticket.com-cert.pem"

  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-seller --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-seller --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-seller --id.name selleradmin --id.secret selleradminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-seller -M "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/seller.ticket.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/msp/config.yaml"

  infoln "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-seller -M "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/tls" --enrollment.profile tls --csr.hosts peer0.seller.ticket.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/seller.ticket.com/peers/peer0.seller.ticket.com/tls/server.key"

  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-seller -M "${PWD}/organizations/peerOrganizations/seller.ticket.com/users/User1@seller.ticket.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/seller.ticket.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/seller.ticket.com/users/User1@seller.ticket.com/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://selleradmin:selleradminpw@localhost:7054 --caname ca-seller -M "${PWD}/organizations/peerOrganizations/seller.ticket.com/users/Admin@seller.ticket.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/seller/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/seller.ticket.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/seller.ticket.com/users/Admin@seller.ticket.com/msp/config.yaml"
}

